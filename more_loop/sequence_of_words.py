#our sententence
sentence = "Hello NACA-120. This is awesome!"
#how we want to split the words
delimeter = " "

#split the words
words = sentence.split(delimeter) #this will give us a sequence of words that we can loop through

#print(words) #show the group of words

#loop through each words
for word in words:
    #print the current word
    print(word)

