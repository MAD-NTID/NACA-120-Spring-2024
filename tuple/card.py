#create a tuple
card = ("Jack", "Diamond", "Red")

#show the tuples
print(card)

#show the length of the tuple: aka, how many elmeents are in the tuple
print(len(card))

#show the first element int he tuple. Remember you can access each element using the index
print(card[0])

#looping through the elements
for element in card:
    print(element)

#show if something is in the tuple
print("Jack" in card)
